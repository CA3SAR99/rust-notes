// Rust Programming notes 1 // 

fn main() // main function, always executes first like in C
{
  println!("Hello World"); // since the function is a "rust macro" we need to include a '!' before the (argument) 
}


// Rust is a compiled language, similar to C.
// You need to compile your Rust code before running it.

// To compile:
// rustc hello_world.rs

// To run the compiled executable:
// ./hello_world   // (on Unix-like systems such as Linux or macOS)
